# String (str)
nome_cliente = "Caio Santos"

# Integer (int)
idade = 28

# Float (float)
preco_produto = 19.99

# Boolean (bool)
esta_ativo = True

# A função print() exibe o valor no terminal
print("Nome do Cliente:", nome_cliente)
print("Idade:", idade)

# A função type() nos mostra o tipo da variável
print("Tipo da variável 'preco_produto':", type(preco_produto))
print("Tipo da variável 'esta_ativo':", type(esta_ativo))